def get_or_create_worksheet(spreadsheet, title, headers):
    """Возвращает лист по имени, создавая его с заголовками при отсутствии."""
    try:
        ws = spreadsheet.worksheet(title)
    except gspread.WorksheetNotFound:
        ws = spreadsheet.add_worksheet(title=title, rows=200, cols=len(headers))
    if not ws.row_values(1):
        ws.append_row(headers)
        ws.format("1:1", {"textFormat": {"bold": True}})
    return ws


def main():
    client = gspread.service_account(filename=config.SERVICE_ACCOUNT_FILE)
    spreadsheet = client.open_by_key(config.SPREADSHEET_ID)
    print(f"Таблица найдена: {spreadsheet.title}")

    animals = get_or_create_worksheet(spreadsheet, config.SHEET_ANIMALS, ANIMALS_HEADERS)
    requests_ws = get_or_create_worksheet(spreadsheet, config.SHEET_REQUESTS, REQUESTS_HEADERS)
    owners = get_or_create_worksheet(spreadsheet, config.SHEET_OWNERS, OWNERS_HEADERS)

